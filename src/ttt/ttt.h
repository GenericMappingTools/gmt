/* Macros for byte-order swapping 2- and 4-byte values (From John M. Kuhn, NOAA) */

#define TTT_swab2(data) ((((data) & 255) << 8) | ((unsigned short) (data) >> 8))
#define TTT_swab4(data) \
	(((data) << 24) | (((data) << 8) & 16711680) | \
	(((data) >> 8) & 65280) | ((unsigned int)(data) >> 24))

