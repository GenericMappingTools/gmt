#if TTT_SIZE_P == 8
	/* 64-bit version of TTT using 8-byte integers to accumulate travel time increments */
	#define TTT_VERSION 	TTT_VER " [64-bit]"	/* ttt version */
	#ifdef _WIN64
	#define TTT_LL "ll"
	#else
	typedef int64_t TTT_LONG;		/* A signed 8 byte integer under OSX/Linux/UNix 64-bit compilation */
	#define TTT_LL "l"
	#endif
	#define LAND		LONG_MAX	/* 9223372036854775807 (64-bit) */
	#define USED		(LONG_MAX - 1)	/* 9223372036854775806 (64-bit) */
	#define OCEAN		(LONG_MAX - 2)	/* 9223372036854775805 (64-bit) */
	#define T_UNIT		0.000000001	/* Integer unit of time in seconds (1 nanosec) for a max tt of ~292 years */
#else	/* TTT_SIZE_P == 4 */
	/* 32-bit version of TTT using 4-byte integers to accumulate travel time increments */
	#define TTT_VERSION 	TTT_VER " [32-bit]"	/* ttt version */
	typedef int TTT_LONG;		/* 4 bytes */
	#define TTT_LL ""	/* No modifier needed */
	#define LAND		INT_MAX		/* 2147483647 (32-bit) */
	#define USED		(INT_MAX - 1)	/* 2147483646 (32-bit) */
	#define OCEAN		(INT_MAX - 2)	/* 2147483645 (32-bit) */
	#define T_UNIT		0.0001		/* Integer unit of time in seconds (0.1 msec) for a max tt of ~119 hours */
#endif

