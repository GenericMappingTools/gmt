#!/usr/bin/env bash
# Test that gmtregress -Fp p-value (column 12) matches MATLAB corrcoef.
# Reference: MATLAB corrcoef gives p = 0.642117440920806
cat << EOF > pvalue.txt
0.488893770311789	-2.94428416199490
1.03469300991786	1.43838029281510
0.726885133383238	0.325190539456198
-0.303440924786016	-0.754928319169703
0.293871467096658	1.37029854009523
-0.787282803758638	-1.71151641885370
0.888395631757642	-0.102242446085491
-1.14707010696915	-0.241447041607358
-1.06887045816803	0.319206739165502
-0.809498694424876	0.312858596637428
EOF
pval=$(gmt regress pvalue.txt -Fp -o12)
echo $pval | awk '{d = $1 - 0.642117440920806; if (d < 0) d = -d; if (d < 1e-6) {print "PASS"} else {print "FAIL: p-value =", $1; exit 1}}'
